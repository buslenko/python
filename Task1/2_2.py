import math

def f22(x):
        a = (x & 0b111) << 16
        b = (x & 0b11111111111000) << 2
        c = (x & 0b1111111100000000000000) << 5
        d = (x & 0b11110000000000000000000000) << 5
        e = (x & 0b11100000000000000000000000000) >> 24
        f = (x & 0b100000000000000000000000000000) >> 28
        g = (x & 0b1000000000000000000000000000000) << 1
        h = (x & 0b10000000000000000000000000000000) >> 31
        return int(g + d + c + a + b + e + f + h)
